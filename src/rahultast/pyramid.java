package rahultast;

public class pyramid {

	public static void main(String[] args) {
		// printing pyramid in java
		
		
		for (int i=1;i<5;i++) 
		{
			
			for (int j=1;j<=i;j++) {
				
				System.out.print("x");
				System.out.print("\t");
			}
			System.out.println();
			
		}
		
		
		
		
		
		
		
		
		
		
		
	}

}
